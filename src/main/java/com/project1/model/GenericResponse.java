package com.project1.model;

public class GenericResponse {
	
	private boolean boolResponse;
	
	
	
	public GenericResponse() {
	}



	/**
	 * @param boolResponse
	 */
	public GenericResponse(boolean boolResponse) {
		super();
		this.boolResponse = boolResponse;
	}



	/**
	 * @return the boolResponse
	 */
	public boolean isBoolResponse() {
		return boolResponse;
	}



	/**
	 * @param boolResponse the boolResponse to set
	 */
	public void setBoolResponse(boolean boolResponse) {
		this.boolResponse = boolResponse;
	}
	
	

}
